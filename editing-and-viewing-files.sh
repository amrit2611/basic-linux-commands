nano filename
#opens file in nano editor

cat file1.txt
#See the content of the file

more file1.txt
#we can scroll down using space (pagewise) or enter (line wise)
#for large files. shows content page-wise

less file1.txt
#we can scroll up or down the file using arrows

q
#to exit from more or less

head -n 5 file1.txt
#shows the first 5 lines of the file 

tail -n 5 file1.txt
#shows the last 5 lines of the file
